template<typename T>
ostream &print_vector(const vector<T> &v, ostream &os)
{
    if (v.size() > 0)
    {
        os << v[0];

        for (typename vector<T>::size_type i = 1; i < v.size(); i++)
        {
            os << ", " << v[i];
        }

        os << endl;
    }

    return os;
}

template<typename T>
ostream &operator<<(ostream &os, const vector<T> &v)
{
    if (v.size() > 0)
    {
        os << "{ ";
        os << v[0];

        for (typename vector<T>::size_type i = 1; i < v.size(); i++)
        {
            os << ", " << v[i];
        }

        os << " }";
    }

    return os;
}

template<typename T>
istream &operator>>(istream &is, vector<T> &d)
{
    char ch1;
    char ch2;
    T temp;
    vector<T> v_temp;
    is >> ch1;
    if (!is)
    {
        return is;
    }
    if (ch1 != '{')
    {
        is.clear(ios_base::failbit);
        return is;
    }
    while (is >> temp && is >> ch2)
    {
        if (ch2 == ',')
        {
            v_temp.push_back(temp);
        }
        else if (ch2 == '}')
        {
            v_temp.push_back(temp);
            d = v_temp;
            return is;
        }
        else
        {
            is.clear(ios_base::failbit);
            return is;
        }
    }

    return is;
}

vector<string>::size_type query(ifstream &infile, const string &query_word)
{
    string str;
    vector<string> text;
    vector<string>::size_type occur_line = 0, cnt = 0;
    map<string, multiset<decltype(occur_line)>> word_line;
    while(getline(infile, str))
    {
        text.push_back(str);
        string word;
        istringstream istrm_line(str);
        while(istrm_line >> word)
            word_line[word].insert(occur_line);
        ++occur_line;
    }
    auto find_rst = word_line.find(query_word);
    if(find_rst != word_line.end())
    {
        cnt = find_rst->second.size();
        std::set<decltype(occur_line)> uniq_lines;
        for(auto num : find_rst->second)
        {
            uniq_lines.insert(num);
        }
        for(auto num : uniq_lines)
            cout << "(line " << num + 1 << "):" << std::endl
               << text[num] << std::endl;
    }
    return cnt;
}

std::string make_plural(size_t ctr, const std::string &word, const std::string &ending)
{
    return (ctr > 1) ? word + ending : word;
}

int main(int argc, const char *argv[])
{
    try
    {
        if(argc != 2)
            return -1;
        while(true)
        {
            ifstream in(argv[1]);
            cout << "enter word to look for, or q to quit: ";
            string s;
            if(!(cin >> s) || s == "q")
                break;
            else
            {
                auto cnt = query(in, s);
                cout << "element occurs " << cnt << make_plural(cnt, " time", "s") << endl;
            }
        }

        return 0;
    }
    catch (exception &e)
    {
        cerr << "error: " << e.what() << '\n';
        return 1;
    }
    catch (...)
    {
        cerr << "Oops: unknown exception!\n";
        return 2;
    }
}
